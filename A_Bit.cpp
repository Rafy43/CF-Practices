//increment and decrement operator
#include<bits/stdc++.h>
using namespace std;
int main(){
    int t,d=0;
    cin>>t;
    while(t--){
        string s;
        cin>>s;
        if(s[1]=='+'){
           d++;
        }
        else{
            d--;
        }
        }
    cout<<d<<endl;
}
